void main(){
  const int a = 20;
  const int b = 12;

  //perform_addition
  int sum = a + b;
  print('Addition: $sum');

  //perform_subtraction
  sum = a - b;
  print('Subtraction: $sum');

  //perform_multiplication
  sum = a * b;
  print('Multiplication: $sum');

  //perform_division
  double result = a / b;
  print('Division: $result');

  //perform_modulus
  int modulusResult = a % b;
  print('Modulus: $modulusResult');

}